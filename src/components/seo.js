import React from 'react';
import Head from 'next/head';

export default function SEO({
  description = 'A Hiring Platform to Manage your talent. Build and engage with your talent network. Access knowledge, insights and opportunities. Search and apply to jobs at 10000+ top companies. Find high paying jobs in IT, software, sales, marketing, operations, finance and more. Hire top talent',
  author = '',
  meta,
  title = 'Staff2hire',
}) {
  const metaData = [
    {
      name: `description`,
      content: description,
    },
    {
      property: `og:title`,
      content: title,
    },
    {
      property: `og:description`,
      content: description,
    },
    {
      property: `og:type`,
      content: `website`,
    },
    {
      name: `twitter:card`,
      content: `summary`,
    },
    {
      name: `twitter:creator`,
      content: author,
    },
    {
      name: `twitter:title`,
      content: title,
    },
    {
      name: `twitter:description`,
      content: description,
    },
  ].concat(meta);
  return (
    <Head>
      <title>{title}</title>
      {metaData.map(({ name, content }, i) => (
        <meta key={i} name={name} content={content} />
      ))}
    </Head>
  );
}

SEO.defaultProps = {
  lang: `en`,
  meta: [],
  description: ``,
};
